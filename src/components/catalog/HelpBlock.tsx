export default function HelpBlock() {
  const [openIndex, setOpenIndex] = useState<number | null>(null);

  const toggle = (index: number) => {
    setOpenIndex(openIndex === index ? null : index);
  };

  return (
    <div className="mt-[60px] w-[640px]">
      {/* HEADER */}
      <div>
        <h2 className="text-[24px] font-semibold">
          Не знаєте, який ноутбук вибрати?
        </h2>

        <p className="mt-[4px] text-[16px] leading-[150%] text-black">
          Ми зібрали відповіді на найпоширеніші запитання, щоб допомогти вам
          зробити правильний вибір.
        </p>
      </div>

      {/* ACCORDION */}
      <div className="mt-[28px] flex flex-col gap-[20px]">
        {faqItems.map((item, index) => {
          const isOpen = openIndex === index;

          return (
            <div key={index}>
              {/* HEADER ROW */}
              <button
                onClick={() => toggle(index)}
                className="w-full h-[47px] flex items-center justify-between border-b border-gray-300"
              >
                <div className="flex items-center gap-[12px]">
                  <span className="text-[18px]">{index + 1}.</span>
                  <span className="text-[18px] text-left">{item.title}</span>
                </div>

                <span className="text-[18px]">{isOpen ? "−" : "+"}</span>
              </button>

              {/* CONTENT */}
              {isOpen && (
                <div className="pt-[10px] text-[16px] leading-[150%] text-gray-700">
                  {item.content}
                </div>
              )}
            </div>
          );
        })}
      </div>
    </div>
  );
}
